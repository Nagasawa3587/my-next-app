#このファイルは、Pythonがディレクトリをパッケージとして認識するために使用されます。
# 具体的には、Pythonの各ディレクトリに __init__.py ファイルが存在することで、そのディレクトリはパッケージとして扱われ、
# 他のPythonファイルからモジュールとしてインポートすることができます。

#主な用途と機能
#1. パッケージの初期化: __init__.py はパッケージがインポートされたときに最初に実行されるスクリプトです。
#このファイル内で、パッケージの初期設定や必要な初期化コードを記述することができます。
#2. 名前空間の管理: __init__.py を使用して、パッケージ内のモジュールを特定の方法で公開したり、隠蔽したりすることができます。
